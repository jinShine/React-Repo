import UIKit

struct ThemeColor {

  var white: UIColor {
    return .white
  }

  var black: UIColor {
    return .black
  }

  var lightGray: UIColor {
    return UIColor(red: 101 / 255, green: 101 / 255, blue: 101 / 255, alpha: 1)
  }

  var line: UIColor {
    return UIColor(red: 220 / 255, green: 220 / 255, blue: 220 / 255, alpha: 1)
  }
}
